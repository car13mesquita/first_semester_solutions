number_grade = int(input("What is your numerical average? "))

if number_grade >= 93:
    print("You got an A")
elif number_grade >= 90:
    print("You got an A-")
elif number_grade >= 87:
    print("You got a B+")
elif number_grade >= 83:
    print("You got a B")
elif number_grade >= 80:
    print("You got a B-")
elif number_grade >= 77:
    print("You got a C+")
elif number_grade >= 73:
    print("You got a C")
elif number_grade >= 70:
    print("You got a C-")
elif number_grade >= 65:
    print("You got a D+")
elif number_grade >= 60:
    print("You got a D")
else:
    print("You got an F")
